#!/usr/bin/env node
/**
 * Bundles a Vercel Function entry file into api/generated-index.ts.
 *
 * Usage:
 *   node packages/core/scripts/build-vercel-api.mjs [entryPoint] [outfile]
 *
 * Defaults:
 *   entryPoint  src/server/vercel-entry.ts
 *   outfile     api/generated-index.ts
 */
import { builtinModules } from 'node:module'
import { build } from 'esbuild'

const [, , entryArg, outfileArg] = process.argv
const entryPoints = [entryArg ?? 'src/server/vercel-entry.ts']
const outfile = outfileArg ?? 'api/generated-index.ts'

const builtins = new Set([
  ...builtinModules,
  ...builtinModules.map((name) => `node:${name}`),
])

const externalizeRuntimeDeps = {
  name: 'externalize-runtime-deps',
  setup(build) {
    build.onResolve({ filter: /^node:/ }, (args) => ({ path: args.path, external: true }))
    build.onResolve({ filter: /^[^./]/ }, (args) => {
      if (builtins.has(args.path)) return { path: args.path, external: true }
      return undefined
    })
  },
}

await build({
  entryPoints,
  outfile,
  bundle: true,
  platform: 'node',
  target: 'node22',
  format: 'esm',
  sourcemap: true,
  banner: {
    js: '// @ts-nocheck\n// Generated by build-vercel-api. Do not edit by hand.\nimport { createRequire } from "node:module";\nconst require = createRequire(import.meta.url);',
  },
  plugins: [externalizeRuntimeDeps],
})
