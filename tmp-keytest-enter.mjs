import { chromium } from 'playwright';
const browser=await chromium.launch({headless:true});
const page=await browser.newPage({viewport:{width:1440,height:900}});
await page.goto('http://127.0.0.1:5213',{waitUntil:'domcontentloaded'});
await page.waitForTimeout(2000);
await page.evaluate(()=>localStorage.setItem('boring-ui:local-workspace-id','boring-ui-v2-36cd3172'));
await page.reload({ waitUntil: 'domcontentloaded' });
await page.waitForTimeout(1000);
await page.keyboard.press('Meta+Shift+K');
await page.waitForTimeout(200);
await page.keyboard.press('ArrowDown');
await page.keyboard.press('Enter');
await page.waitForTimeout(1000);
const stored=await page.evaluate(()=>localStorage.getItem('boring-ui:local-workspace-id'));
console.log(JSON.stringify({stored}));
await browser.close();
