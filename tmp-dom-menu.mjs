import { chromium } from 'playwright';
const browser=await chromium.launch({headless:true});
const page=await browser.newPage({viewport:{width:1440,height:900}});
await page.goto('http://213.32.19.186:5213',{waitUntil:'domcontentloaded'});
await page.waitForTimeout(2000);
await page.keyboard.press('Meta+Shift+K');
await page.waitForTimeout(200);
await page.keyboard.press('ArrowDown');
await page.waitForTimeout(500);
const rows = await page.locator('[data-slot="dropdown-menu-item"]').evaluateAll(els => els.map(e => ({text:e.textContent, active:e.getAttribute('data-active'), current:e.getAttribute('data-current'), cls:e.getAttribute('class'), focused: e === document.activeElement})));
console.log(JSON.stringify(rows,null,2));
await browser.close();
